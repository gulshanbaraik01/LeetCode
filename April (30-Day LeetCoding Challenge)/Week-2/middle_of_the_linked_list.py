# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def middleNode(self, head: ListNode) -> ListNode:
        temp = head
        
        while head is not None and head.next is not None:
            temp = temp.next
            head = head.next.next
            
        return temp